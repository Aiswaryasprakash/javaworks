import java.util.Scanner;
class abinclusive{
public static void main(String[] args){
Scanner input=new Scanner(System.in);
System.out.println("enter a");
int a=input.nextInt();
System.out.println("enter b");
int b=input.nextInt();
System.out.println("Output:");
for(int i=a;i<=b;i++)
{
System.out.println(i);
}
}
}